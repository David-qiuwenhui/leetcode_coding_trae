/**
 * 计算两个数字的和
 * @param {number} a - 第一个数字
 * @param {number} b - 第二个数字
 * @returns {number} 两个数字的和
 */
export function add(a, b) {
  return a + b;
}

/**
 * 判断一个数字是否为偶数
 * @param {number} num - 要判断的数字
 * @returns {boolean} 如果是偶数返回true，否则返回false
 */
export function isEven(num) {
  return num % 2 === 0;
}